package com.syntax.class07;

public class DoLoopDemo {

	public static void main(String[] args) {

		int num=1;
		while (num<=5) { // is used more than do while
			System.out.println("Hello"); //5 times
			num++;
		}
		
		System.out.println();
		System.out.println("-------------------");
		
		int num1=1;		
		do { //is less used than while
			System.out.println("Hello");
			num1++;
		}while (num1<=3);
		
		System.out.println();
		System.out.println("-------------------");
		
		int num2=30;		
		do { 
			System.out.print(num2+" ");
			num2--;
		}while (num2>=1);
		
		System.out.println();
		System.out.println("-------------------");
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
