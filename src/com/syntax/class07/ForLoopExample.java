package com.syntax.class07;

public class ForLoopExample {

	public static void main(String[] args) {
		/*
		 * Initialization
		 * Conditions
		 * Increment || decrement
		 * 
		 * FOR loop is used when the number of repetition is known 
		 * 
		 */
		for (int i=1; i<=5; i++) {
			System.out.println("hello");
		}
		System.out.println();
		System.out.println("-------------------------------------------");
		System.out.println("I need to print numbers from 1 to 20");
		
		/*
		 * start point
		 * end point
		 */
		
		for (int i=1; i<=20; i++) {			
			System.out.print(i+" ");
			
		}		
		System.out.println();
		System.out.println("-------------------------------------------");
		System.out.println("I need to print numbers from 10 to 25");
		for (int j=10; j<=25; j++) {
			System.out.print(j+" ");
		}
		System.out.println();
		System.out.println("-------------------------------------------");
		System.out.println("I need to print numbers from 50 to 1");
		for (int k=50; k>=1; k--) {
			System.out.print(k+" ");
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
