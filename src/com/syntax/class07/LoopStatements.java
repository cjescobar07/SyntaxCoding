package com.syntax.class07;

public class LoopStatements {

	public static void main(String[] args) {

		// ++ - this means an increment of 1 to a variable
		
		int time=10;
		if(time<12) {
			System.out.println("Morning");//1
		}
		System.out.println("----------------------");
		
		while(time<12) {
			System.out.println("Morning");
			
		time++;
		
		}

	}

}
